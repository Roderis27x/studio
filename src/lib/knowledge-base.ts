export function formatKnowledgeBase(): string {
  const sections: string[] = [];

  sections.push('=== INFORMACIÓN SOBRE CPT-SOFT ===\n');
  sections.push(`Nombre: ${knowledgeBase.empresa.nombre}`);
  sections.push(`Descripción: ${knowledgeBase.empresa.descripcion}\n`);

  sections.push('=== MÓDULOS DISPONIBLES ===');
  Object.entries(knowledgeBase.modulos).forEach(([name, info]) => {
    sections.push(`\n${name}:`);
    sections.push(`  Descripción: ${info.descripcion}`);
    sections.push(`  Características: ${info.caracteristicas.join(', ')}`);
  });

  sections.push('\n\n=== SERVICIOS OFRECIDOS ===');
  Object.entries(knowledgeBase.servicios).forEach(([name, info]) => {
    sections.push(`\n${name}:`);
    sections.push(`  Descripción: ${info.descripcion}`);
    sections.push(`  Beneficios: ${info.beneficios.join(', ')}`);
  });

  sections.push('\n\n=== VENTAJAS ===');
  sections.push(knowledgeBase.caracteristicas.ventajas.join('\n'));

  sections.push('\n\n=== PREGUNTAS FRECUENTES ===');
  Object.entries(knowledgeBase.preguntas_frecuentes).forEach(([question, answer]) => {
    sections.push(`\nP: ${question}`);
    sections.push(`R: ${answer}`);
  });

  return sections.join('\n');
}
